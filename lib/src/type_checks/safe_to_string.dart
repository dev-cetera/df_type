String safeToString(Object? obj) {
  try {
    return obj.toString();
  } catch (e) {
    assert(false, e);
    return '${obj.runtimeType}@${obj.hashCode.toRadixString(16)}';
  }
}

extension SafeToStringOnObjectX on Object {
  String safeToString() => _safeToString(this);
}

final _safeToString = safeToString;
